#!/usr/bin/env node

import { spawnSync } from "node:child_process";

const BLOCKED_PATTERNS = [
  {
    id: "os-junk",
    pattern: /(^|\/)\.DS_Store$/,
    reason: "OS metadata must stay local.",
  },
  {
    id: "dependency-install",
    pattern: /(^|\/)node_modules\//,
    reason: "Dependency installs are restored by pnpm and must not be tracked.",
  },
  {
    id: "runtime-cache",
    pattern: /(^|\/)(\.cache|\.build-cache|playwright-report|\.wrangler)\//,
    reason: "Runtime caches and local reports must stay untracked.",
  },
  {
    id: "scratch-output",
    pattern: /(^|\/)(\.tmp|tmp|test-results)\//,
    reason: "Scratch and test-output directories must stay untracked.",
  },
  {
    id: "coverage-output",
    pattern: /(^|\/)coverage\//,
    reason: "Coverage output is regenerated by test runs.",
  },
  {
    id: "typescript-build-info",
    pattern: /\.tsbuildinfo$/,
    reason: "TypeScript incremental build state is machine-local.",
  },
  {
    id: "mutable-build-output",
    pattern: /(^|\/)dist\/|^platforms\/web\/apps\/storybook\/screenshots\//,
    reason: "Build and screenshot outputs are regenerated by build or visual gates.",
  },
  {
    id: "audit-output",
    pattern: /^reports\/audit-/,
    reason: "Ad hoc audit artifacts belong outside source-control authority.",
  },
];

const DOCUMENTED_EXCEPTION_PATTERNS = [
  /^\.agent\//,
  /^\.changeset\//,
  /^\.claude\//,
  /^\.codex\/environments\//,
  /^\.figma\//,
  /^\.kiro\//,
  /^\.spec\//,
  /^\.vale\/styles\//,
  /^packages\/widgets\/src\/sdk\/generated\/widget-manifest\.ts$/,
];

const SELF_TEST_CASES = [
  [".DS_Store", true],
  ["node_modules/.vite/deps/react.js", true],
  ["packages/ui/node_modules/.tmp/build.log", true],
  ["coverage/lcov.info", true],
  ["packages/ui/tsconfig.tsbuildinfo", true],
  ["platforms/web/apps/storybook/screenshots/button.png", true],
  ["packages/ui/dist/index.js", true],
  ["reports/audit-screenshot-home.png", true],
  [".agent/EXECPLAN-agent-browser-smoke-tests.md", false],
  [".spec/archive/SPEC_INDEX.md", false],
  [".kiro/specs/ui-design-system/design.md", false],
  [".changeset/config.json", false],
  [".codex/environments/environment.toml", false],
  [".vale/styles/Google/Acronyms.yml", false],
  ["packages/widgets/src/sdk/generated/widget-manifest.ts", false],
];

function normalizePath(filePath) {
  return filePath.replace(/\\/g, "/").replace(/^\.\//, "");
}

function isDocumentedException(filePath) {
  return DOCUMENTED_EXCEPTION_PATTERNS.some((pattern) => pattern.test(filePath));
}

function classifyTrackedIgnoredPath(filePath) {
  const normalized = normalizePath(filePath);
  if (isDocumentedException(normalized)) {
    return null;
  }

  return BLOCKED_PATTERNS.find(({ pattern }) => pattern.test(normalized)) ?? null;
}

function listTrackedIgnoredPaths() {
  const result = spawnSync("git", ["ls-files", "-ci", "--exclude-standard"], {
    cwd: process.cwd(),
    encoding: "utf8",
    stdio: "pipe",
  });

  if (result.status !== 0) {
    if (result.stdout) process.stdout.write(result.stdout);
    if (result.stderr) process.stderr.write(result.stderr);
    console.error("tracked-ignored: failed to list ignored tracked files.");
    process.exit(result.status ?? 1);
  }

  return result.stdout
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);
}

function runSelfTest() {
  const failures = [];

  for (const [filePath, shouldBlock] of SELF_TEST_CASES) {
    const blocked = classifyTrackedIgnoredPath(filePath) !== null;
    if (blocked !== shouldBlock) {
      failures.push({ filePath, expected: shouldBlock, actual: blocked });
    }
  }

  if (failures.length > 0) {
    console.error("tracked-ignored: classifier self-test failed:");
    for (const failure of failures) {
      console.error(
        `  - ${failure.filePath}: expected blocked=${failure.expected}, got blocked=${failure.actual}`,
      );
    }
    process.exit(1);
  }

  console.log("tracked-ignored: classifier self-test ok");
}

if (process.argv.includes("--self-test")) {
  runSelfTest();
  process.exit(0);
}

const trackedIgnoredPaths = listTrackedIgnoredPaths();
const violations = trackedIgnoredPaths
  .map((filePath) => ({
    filePath,
    classification: classifyTrackedIgnoredPath(filePath),
  }))
  .filter(({ classification }) => classification !== null);

if (violations.length > 0) {
  console.error("tracked-ignored: prohibited ignored artifacts are tracked:");
  for (const { filePath, classification } of violations) {
    console.error(`  - ${filePath} [${classification.id}] ${classification.reason}`);
  }
  console.error(
    "tracked-ignored: remove these paths from the Git index or document a narrower exception before rerunning.",
  );
  process.exit(1);
}

console.log(
  `tracked-ignored: ok (${trackedIgnoredPaths.length} ignored tracked path(s), 0 prohibited runtime/build artifacts)`,
);

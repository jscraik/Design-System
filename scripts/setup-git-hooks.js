#!/usr/bin/env node
/**
 * Install canonical prek git hooks for this repository.
 *
 * Run this script from the repository root:
 *   node scripts/setup-git-hooks.js
 *
 * This script:
 *   1. Removes legacy package.json hook wiring when present
 *   2. Clears local core.hooksPath overrides so git can use installed hooks
 *   3. Runs `prek install`
 */

import { execFileSync } from "node:child_process";
import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const PACKAGE_JSON_PATH = resolve(process.cwd(), "package.json");
const REQUIRED_SCRIPTS = {
  "codestyle:validate": "bash scripts/validate-codestyle.sh",
  "secrets:staged": "bash scripts/check-staged-secrets.sh",
  "docs:style:changed": "bash scripts/check-doc-style.sh",
  "test:related": "bash scripts/check-related-tests.sh",
  "semgrep:changed": "bash scripts/check-semgrep-changed.sh",
};
const PREK_HOOK_MARKER = "# File generated by prek: https://github.com/j178/prek";
const PREK_HOME_EXPORT = `PREK_HOME="\${PREK_HOME:-$HERE/../.cache/prek}"`;
const PREK_HOOK_PATCH = [
  "# Keep prek cache/logs in repo-local .git to avoid home-dir sandbox write failures",
  PREK_HOME_EXPORT,
  'mkdir -p "$PREK_HOME" 2>/dev/null || true',
  "export PREK_HOME",
  "",
].join("\n");

const SIMPLE_GIT_HOOKS_BOOTSTRAP_PATTERN = /^(?:npx\s+)?simple-git-hooks(?:\s+install)?$/;

function patchInstalledPrekHooks() {
  let hooksDir;
  try {
    hooksDir = execFileSync("git", ["rev-parse", "--git-path", "hooks"], {
      encoding: "utf8",
      cwd: process.cwd(),
    }).trim();
    if (!hooksDir) {
      throw new Error("git returned an empty hooks directory");
    }
  } catch (error) {
    throw new Error(
      `failed to resolve git hooks directory for marker ${PREK_HOOK_MARKER}: ${
        error instanceof Error ? error.message : String(error)
      }`,
    );
  }

  if (!existsSync(hooksDir)) {
    throw new Error(`git hooks directory does not exist: ${hooksDir}`);
  }

  let patchedCount = 0;
  for (const hookName of readdirSync(hooksDir)) {
    const hookPath = resolve(hooksDir, hookName);
    if (!statSync(hookPath).isFile()) {
      continue;
    }
    let hookContent = "";
    try {
      hookContent = readFileSync(hookPath, "utf8");
    } catch (error) {
      throw new Error(
        `failed to read installed hook "${hookName}" while applying ${PREK_HOME_EXPORT}: ${
          error instanceof Error ? error.message : String(error)
        }`,
      );
    }

    if (!hookContent.includes(PREK_HOOK_MARKER)) {
      continue;
    }
    if (hookContent.includes(PREK_HOME_EXPORT)) {
      continue;
    }

    const patched = hookContent.replace(
      'fi\n\nexec "$PREK" hook-impl',
      `fi\n\n${PREK_HOOK_PATCH}exec "$PREK" hook-impl`,
    );
    if (patched === hookContent) {
      console.warn(
        `⚠ Hook "${hookName}" has the prek marker but could not be patched (shim format may have changed)`,
      );
      continue;
    }

    try {
      writeFileSync(hookPath, patched);
    } catch (error) {
      throw new Error(
        `failed to patch installed hook "${hookName}" with PREK_HOOK_PATCH: ${
          error instanceof Error ? error.message : String(error)
        }`,
      );
    }
    patchedCount += 1;
  }

  return patchedCount;
}

function splitCommandSequence(command) {
  return command
    .split(/&&|;/)
    .map((segment) => segment.trim())
    .filter(Boolean);
}

function isLegacySimpleGitHooksBootstrap(command) {
  return SIMPLE_GIT_HOOKS_BOOTSTRAP_PATTERN.test(command.trim());
}

function getGitConfigValue(...args) {
  try {
    return execFileSync("git", ["config", ...args], { encoding: "utf8" }).trim();
  } catch {
    return "";
  }
}

function main() {
  if (!existsSync(PACKAGE_JSON_PATH)) {
    console.error("Error: package.json not found in current directory");
    console.error("  Run this script from your project root.");
    process.exit(1);
  }

  let packageJson;
  try {
    packageJson = JSON.parse(readFileSync(PACKAGE_JSON_PATH, "utf-8"));
  } catch {
    console.error("Error: Failed to parse package.json");
    process.exit(1);
  }

  let modified = false;

  // Ensure devDependencies exists
  if (!packageJson.devDependencies) {
    packageJson.devDependencies = {};
  }

  if (packageJson.devDependencies?.["simple-git-hooks"]) {
    delete packageJson.devDependencies["simple-git-hooks"];
    console.info("✓ Removed legacy hook dependency metadata");
    modified = true;
  }

  // Ensure scripts exists
  if (!packageJson.scripts) {
    packageJson.scripts = {};
  }

  const scripts = packageJson.scripts;
  if (typeof scripts.postinstall === "string") {
    const postinstallSteps = splitCommandSequence(scripts.postinstall);
    const legacyBootstrapSteps = postinstallSteps.filter((step) =>
      isLegacySimpleGitHooksBootstrap(step),
    );
    if (legacyBootstrapSteps.length > 0) {
      if (legacyBootstrapSteps.length !== postinstallSteps.length) {
        console.error(
          "Error: scripts.postinstall mixes simple-git-hooks bootstrap with other commands.\n" +
            "Remove the simple-git-hooks segment manually before running setup-git-hooks.",
        );
        process.exit(1);
      }

      delete scripts.postinstall;
      console.info("✓ Removed legacy hook postinstall bootstrap");
      modified = true;
    }
  }

  // Enforce required helper scripts used by the hook targets
  const mergedScripts = { ...scripts, ...REQUIRED_SCRIPTS };
  if (JSON.stringify(scripts) !== JSON.stringify(mergedScripts)) {
    packageJson.scripts = mergedScripts;
    console.info("✓ Enforced required hook helper scripts");
    modified = true;
  } else {
    console.info("✓ Required hook helper scripts already present");
  }

  if (packageJson["simple-git-hooks"]) {
    delete packageJson["simple-git-hooks"];
    console.info("✓ Removed legacy top-level hook configuration");
    modified = true;
  }

  // Write changes if modified
  if (modified) {
    writeFileSync(PACKAGE_JSON_PATH, `${JSON.stringify(packageJson, null, 2)}\n`);
    console.info("\n✓ package.json updated");
  }

  try {
    execFileSync("git", ["config", "--local", "--unset-all", "core.hooksPath"], {
      stdio: "ignore",
    });
    console.info("✓ Cleared local core.hooksPath override");
  } catch {
    console.info("✓ No local core.hooksPath override set");
  }

  const effectiveHooksPath = getGitConfigValue("core.hooksPath");
  if (effectiveHooksPath) {
    console.warn(
      `⚠ Effective core.hooksPath is set to "${effectiveHooksPath}".\n` +
        "Proceeding without overriding it; hooks will install to that configured path.",
    );
    const originEntries = getGitConfigValue("--show-origin", "--get-all", "core.hooksPath");
    if (originEntries) {
      console.warn("\nDetected core.hooksPath sources:");
      console.warn(originEntries);
    }
  } else {
    console.info("✓ No effective core.hooksPath override remains");
  }

  console.info("\nInstalling prek git hooks...");
  try {
    execFileSync("prek", ["install"], { stdio: "inherit" });
    const patchedCount = patchInstalledPrekHooks();
    if (patchedCount > 0) {
      console.info(`✓ Patched ${patchedCount} prek hook shim(s) with repo-local PREK_HOME`);
    }
    console.info("\n✓ Git hooks installed and active!");
    console.info("\nHooks enabled:");
    console.info("  • pre-commit: make hooks-pre-commit");
    console.info("  • commit-msg: validates conventional commit format");
    console.info("  • pre-push: make hooks-pre-push");
  } catch (error) {
    console.error(
      `\n✗ setup-git-hooks failed: ${error instanceof Error ? error.message : String(error)}`,
    );
    process.exitCode = 1;
  }
}

main();
